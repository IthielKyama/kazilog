const jwt = require('jsonwebtoken');
const crypto = require('crypto');
const asyncHandler = require('../utils/asyncHandler');
const User = require('../models/User');
const sendEmail = require('../utils/sendEmail');
const {
  assertStrongPassword,
  generateTemporaryPassword,
  PASSWORD_POLICY_MESSAGE,
} = require('../utils/passwordPolicy');

const getWebDashboardUrl = () =>
  (process.env.WEB_DASHBOARD_URL || 'http://localhost:5173').replace(/\/+$/, '');

// Generate JWT Token
const generateToken = (id) => {
  return jwt.sign({ id }, process.env.JWT_SECRET, {
    expiresIn: '30d',
  });
};

const buildAuthResponse = (user) => ({
  _id: user.id,
  name: user.name,
  email: user.email,
  role: user.role,
  mustChangePassword: Boolean(user.mustChangePassword),
  token: generateToken(user._id),
});

// @desc    Register a new user
// @route   POST /api/auth/register
// @access  Public
exports.registerUser = asyncHandler(async (req, res) => {
  const { name, email, registrationNumber, department } = req.body;
  let { password, role } = req.body;

  if (!name || !email) {
    res.status(400);
    throw new Error('Please add all basic fields');
  }

  // Role restriction: only admins can assign non-student roles
  if (role && role !== 'student') {
    if (!req.user || req.user.role !== 'admin') {
      res.status(403);
      throw new Error('Only admins can create non-student accounts');
    }
  } else {
    role = 'student';
  }

  if (role === 'student' && !registrationNumber) {
    res.status(400);
    throw new Error('Registration number is required for student accounts');
  }

  // Check if user exists
  const userExists = await User.findOne({ email });

  if (userExists) {
    res.status(400);
    throw new Error('User already exists');
  }

  // Auto-generate password if not provided
  let autoGeneratedPassword = null;
  if (!password) {
    autoGeneratedPassword = generateTemporaryPassword();
    password = autoGeneratedPassword;
  } else {
    res.status(400);
    assertStrongPassword(password);
  }

  // Create user
  const user = await User.create({
    name,
    email,
    password,
    role,
    registrationNumber: role === 'student' ? registrationNumber : undefined,
    department,
    mustChangePassword: Boolean(autoGeneratedPassword),
  });

  if (user) {
    // If we auto-generated a password, send the welcome email!
    if (autoGeneratedPassword) {
      const message = `Welcome to KaziLog!\n\nYour account has been created by the System Administrator. You can now log into your dashboard.\n\nYour Temporary Password is: ${autoGeneratedPassword}\n\nYou must change this password the first time you sign in.\n\nPassword requirements: ${PASSWORD_POLICY_MESSAGE}`;
      
      try {
        await sendEmail({
          email: user.email,
          subject: 'KaziLog - Your Account Details',
          message
        });
      } catch (err) {
        console.error('Error sending welcome email:', err);
      }
    }

    res.status(201).json(buildAuthResponse(user));
  } else {
    res.status(400);
    throw new Error('Invalid user data');
  }
});

// @desc    Authenticate a user
// @route   POST /api/auth/login
// @access  Public
exports.loginUser = asyncHandler(async (req, res) => {
  const { email, password } = req.body;

  // Check for user email
  const user = await User.findOne({ email }).select('+password');

  if (user && (await user.matchPassword(password))) {
    res.json(buildAuthResponse(user));
  } else {
    res.status(401);
    throw new Error('Invalid credentials');
  }
});

